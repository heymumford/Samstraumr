/**
 * Domain component composite package contains entities and logic for component composition.
 * <p>
 * This package defines the composite pattern implementation for components, allowing
 * individual components to be composed into larger structures while maintaining the
 * same interface. This enables hierarchical composition of processing elements.
 * </p>
 * <p>
 * Key concepts in this package:
 * <ul>
 *   <li>CompositeComponent - A component that contains other components</li>
 *   <li>ComponentConnection - Defines connections between components</li>
 *   <li>ConnectionType - Different types of connections between components</li>
 *   <li>CompositeType - Different types of component compositions</li>
 * </ul>
 * </p>
 * <p>
 * This package implements the Composite design pattern, allowing uniform treatment
 * of both individual components and compositions of components. The composites
 * maintain the Component interface while adding composition-specific capabilities.
 * </p>
 */
package org.s8r.domain.component.composite;