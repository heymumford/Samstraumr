package org.s8r.application.port.notification;

/**
 * Enumeration of content formats for notifications.
 */
public enum ContentFormat {
    /**
     * Plain text format.
     */
    TEXT,
    
    /**
     * HTML format.
     */
    HTML,
    
    /**
     * Markdown format.
     */
    MARKDOWN,
    
    /**
     * JSON format.
     */
    JSON,
    
    /**
     * XML format.
     */
    XML
}